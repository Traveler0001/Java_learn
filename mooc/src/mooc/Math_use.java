package mooc;

public class Math_use {
	
	public static void eg1() {
		System.out.println(Math.abs(-12));
		//求绝对值
		System.out.println(Math.round(25.2222));
		//四舍五入取整
		System.out.println(Math.random());
		//产生随机数，范围[0，1)
		System.out.println(Math.pow(23, 3));
		//x的y次幂，可以为小数次幂，例如2，3.2
	}
	
	public static void main(String[] args) {
		// TODO 自动生成的方法存根
		eg1();
	}

}
